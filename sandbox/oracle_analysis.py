"""Phase 2: perfect-foresight upper bounds (diagnostic only -- not a policy).

Oracle A: independent per-battery exact DP using the REAL realized RT price
trajectory for that instance (network constraints ignored -- optimistic).

Oracle B: network-feasible perfect-foresight LP over the whole horizon using
the same realized RT trajectory, with PTDF flow constraints (scipy HiGHS).
The quadratic degradation cost term is dropped in the LP objective (it is
economically negligible at this problem's scale: KAPPA_DEG*(power*dt/capacity)^2
with power/capacity ~ 0.25 gives ~1e-5 $ per MW of action, vs price terms of
tens of dollars) -- this makes Oracle B a very slightly optimistic, clearly
labeled upper bound, not a claimed exact optimum.

The realized RT price trajectory is generated by the episode RNG independently
of any policy's actions (verified from challenge.py: take_step's next-seed draw
happens before the action-dependent state update, and price generation only
consumes that seed) -- so it can be recorded once with any placeholder policy.
"""
import sys
